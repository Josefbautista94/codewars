function XO(str) {

  let newArr = str.toUpperCase().split('')
  let counterX = 0; 
  let counterO = 0;
  
  
  for(let i = 0; i < newArr.length; i++){
    if (newArr[i] == 'X'){
      counterX ++
    }
    if(newArr[i] == 'O'){
      counterO ++
    }
  }
  if (counterX  === counterO){
    return true
  }
   return false;
}
